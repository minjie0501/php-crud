<?php

class Teacher{

    private $id;
    private $name;
    private $email;
    private $allStudents;

    function __construct()
    {
        
    }

    public function getId(){
        $this->id;
    }

    public function getName(){
        $this->name;
    }

    public function getEmail(){
        $this->email;
    }

    public function getAllStudents(){
        $this->allStudents;
    }
}

?>